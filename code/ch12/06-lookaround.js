// 本文件演示：前瞻 (?=) (?!) 与后顾 (?<=) (?<!)。它们是"条件"，不消耗字符。

// 正向前瞻 (?=...)：要求右边"跟着"某模式，但不把它算进匹配
// 匹配后面跟着 "px" 的数字（但不包含 px 本身）
console.log("前瞻:", "10px 20em 30px".match(/\d+(?=px)/g)); // ['10','30']

// 负向前瞻 (?!...)：要求右边"不跟着"某模式
console.log("负前瞻:", "10px 20em 30px".match(/\d+(?!px|\d)/g)); // ['20']

// 正向后顾 (?<=...)：要求左边"前面是"某模式
console.log("后顾:", "$100 ￥200 $300".match(/(?<=\$)\d+/g)); // ['100','300']

// 负向后顾 (?<!...)：要求左边"前面不是"某模式
console.log("负后顾:", "$100 ￥200".match(/(?<!\$)\b\d+/g)); // ['200']

// 实用例子：给数字加千分位逗号（每 3 位一逗号）
const n = "1234567";
const withCommas = n.replace(/\B(?=(\d{3})+(?!\d))/g, ",");
console.log("千分位:", withCommas); // 1,234,567
